#!/usr/bin/env python3
"""Find node_modules directories and report how much space they occupy."""

from __future__ import annotations

import argparse
import os
from pathlib import Path


def format_size(size: int) -> str:
    units = ("B", "KB", "MB", "GB", "TB")
    value = float(size)

    for unit in units:
        if value < 1024 or unit == units[-1]:
            if unit == "B":
                return f"{int(value)} {unit}"
            return f"{value:.2f} {unit}"
        value /= 1024

    return f"{size} B"


def directory_size(path: Path) -> int:
    total = 0

    for root, _, files in os.walk(path, onerror=lambda error: None):
        root_path = Path(root)
        for file_name in files:
            file_path = root_path / file_name
            try:
                total += file_path.lstat().st_size
            except OSError:
                continue

    return total


def find_node_modules(root: Path) -> list[Path]:
    matches: list[Path] = []

    for current_root, dirs, _ in os.walk(root, onerror=lambda error: None):
        if "node_modules" not in dirs:
            continue

        node_modules_path = Path(current_root) / "node_modules"
        matches.append(node_modules_path)
        dirs.remove("node_modules")

    return sorted(matches)


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Find all node_modules folders below a project root and show disk usage."
    )
    parser.add_argument(
        "root",
        nargs="?",
        default=".",
        help="Project root to scan. Defaults to the current directory.",
    )
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    root = Path(args.root).expanduser().resolve()

    if not root.is_dir():
        print(f"Not a directory: {root}")
        return 1

    matches = find_node_modules(root)
    if not matches:
        print(f"No node_modules directories found under {root}")
        return 0

    rows: list[tuple[int, Path]] = []
    for path in matches:
        rows.append((directory_size(path), path))

    rows.sort(reverse=True)
    total_size = sum(size for size, _ in rows)
    width = max(len(format_size(size)) for size, _ in rows)

    print(
        f"Found {len(rows)} node_modules director{'y' if len(rows) == 1 else 'ies'} under {root}"
    )
    print()
    for size, path in rows:
        print(f"{format_size(size):>{width}}  {path.relative_to(root)}")

    print()
    print(f"Total: {format_size(total_size)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
